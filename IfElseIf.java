package com.tns.basic;

public class IfElseIf {
	public static void main(String[] args) {
		String city = "Mumbai";
		if(city == "Nashik") {
			System.out.println("City is Nashik");
		}else if (city == "Pune"){
			System.out.println("City is Pune");
		}else {
			System.out.println(city);
		}
	}
}
